/**
 * Create a translator function for a given language.
 * Usage: const t = createT('id'); t('scanner.scanTitle') returns "Scan QRIS"
 * Falls back to English, then to an empty string to prevent raw keys in UI.
 */
export const createT = (lang) => {
  const resolve = (obj, path) => {
    const parts = path.split(".");
    let current = obj;
    for (const part of parts) {
      if (current == null || typeof current !== "object") return undefined;
      current = current[part];
    }
    return current;
  };

  return (key) => {
    const primary = resolve(translations[lang], key);
    if (primary !== undefined && primary !== null) return primary;
    if (lang !== "en") {
      const fallback = resolve(translations.en, key);
      if (fallback !== undefined && fallback !== null) return fallback;
    }
    if (import.meta.env.DEV) {
      console.warn("[Translations] Missing key:", key);
    }
    return "";
  };
};
